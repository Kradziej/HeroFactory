package hero;

public class Hero {
	
	private String name;
	private HeroType type;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public HeroType getType() {
		return type;
	}
	public void setType(HeroType type) {
		this.type = type;
	}
	

}
